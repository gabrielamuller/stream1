var Calculator = {
    add: function (add1, add2) {
        return add1 + add2; 
    },
    sub: function (sub1, sub2) {
        return sub1 - sub2; 
    },
    div: function (div1, div2) {
        return div2 && div1/div2; 
    },
    mul: function (mult1, mult2) {
        return mult1 * mult2; 
    }
};